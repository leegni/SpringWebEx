package com.care.Membership;

public class Member extends Login {

	private String memEmail;
	private String memPostCode;
	private String memAdd;
	private String memDetailAdd;
	private String memGender;
	

	public String getMemEmail() {
		return memEmail;
	}
	public void setMemEmail(String memEmail) {
		this.memEmail = memEmail;
	}
	public String getMemPostCode() {
		return memPostCode;
	}
	public void setMemPostCode(String memPostCode) {
		this.memPostCode = memPostCode;
	}
	public String getMemAdd() {
		return memAdd;
	}
	public void setMemAdd(String memAdd) {
		this.memAdd = memAdd;
	}
	public String getMemDetailAdd() {
		return memDetailAdd;
	}
	public void setMemDetailAdd(String memDetailAdd) {
		this.memDetailAdd = memDetailAdd;
	}
	public String getMemGender() {
		return memGender;
	}
	public void setMemGender(String memGender) {
		this.memGender = memGender;
	}

	
}
